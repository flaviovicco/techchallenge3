# Esta etapa gera resumos autom√°ticos a partir do dataset original.
# Posteriormente, os pares (descri√ß√£o ‚Üí resumo) ser√£o usados no fine-tuning.

from transformers import pipeline
import json
from tqdm import tqdm

# Carrega modelo de sumariza√ß√£o BART da Hugging Face.
summarizer = pipeline("summarization", model="facebook/bart-large-cnn")

def summarize_amazon(jsonl_file, output_file="amazon_summaries_stream.json", flush_every=10):
    """
    Fun√ß√£o que gera resumos autom√°ticos a partir de descri√ß√µes.
    Grava os resultados em disco de forma incremental (streaming) para economizar mem√≥ria.
    """
    count = 0

    # Conta n√∫mero total de linhas para exibir progresso.
    with open(jsonl_file, 'r') as f:
        total_lines = sum(1 for _ in f)

    # Leitura linha a linha e gera√ß√£o de resumo.
    with open(jsonl_file, 'r') as f_in, open(output_file, 'w') as f_out:
        f_out.write('{"amazon_summaries": [\n')
        first_item = True

        # tqdm fornece uma barra de progresso.
        for line in tqdm(f_in, total=total_lines, desc="Gerando resumos"):
            try:
                item = json.loads(line)
                title = item.get("title", "")
                content = item.get("content", "")
                text = f"Title: {title}\nDescription: {content}"

                # Gera o resumo com BART.
                summary = summarizer(
                    text,
                    max_length=20,
                    min_length=15,
                    do_sample=False
                )[0]["summary_text"]

            except Exception as e:
                summary = f"[ERROR: {e}]"

            # Monta o dicion√°rio com o resumo gerado.
            result = {"title": title, "content": content, "summary": summary}

            # Grava no arquivo incrementalmente.
            if not first_item:
                f_out.write(",\n")
            json.dump(result, f_out, ensure_ascii=False)
            first_item = False

            count += 1
            if count % flush_every == 0:
                f_out.flush()  # grava no disco periodicamente

        f_out.write("\n]}\n")

    print(f"\n‚úÖ Conclu√≠do! {count} produtos processados e salvos em {output_file}")

# Executa a gera√ß√£o de resumos
summarize_amazon("/content/drive/MyDrive/Tech Challenge 3/tst2.json")

